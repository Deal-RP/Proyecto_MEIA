    private void BT_EnviarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BT_EnviarActionPerformed
        var dataUser = Data.getData();
        var user = dataUser.getUser();
        var strError = "";
        var listaEnviar = new DefaultListModel();
        if(RB_Lista.isSelected()){
            var objManejo = new ManejoArchivo();
            var lista = objManejo.lecturaCompleta("Lista_usuario", CB_Envios.getSelectedItem().toString(), user);
            for (int i = 0; i < lista.size(); i++) {
                var splitAux = lista.get(i).split(Pattern.quote("|"));
                listaEnviar.addElement(splitAux[3]);
            }
            DateFormat dateFormat = new SimpleDateFormat("yyyy/MM/dd HH:mm:ss");
            Date date = new Date();
            for (int i = 0; i < listaEnviar.getSize(); i++) {
                //Insertar en arbol de correo
                var todosLosRegistros = user + "|" + listaEnviar.get(i) + "|" + dateFormat.format(date) + "|" 
                        + TF_Asunto.getText() + "|" + TA_Mensaje.getText() + "|" + TF_Archivo.getText() + "1";
            }
        }
        else if(RB_Usuario.isSelected()){
            DateFormat dateFormat = new SimpleDateFormat("yyyy/MM/dd HH:mm:ss");
            Date date = new Date();
                //Insertar en arbol de correo
            var todosLosRegistros = user + "|" + CB_Envios.getSelectedItem().toString() + "|" + dateFormat.format(date) + "|" 
                        + TF_Asunto.getText() + "|" + TA_Mensaje.getText() + "|" + TF_Archivo.getText() + "1";
        }
        else{
            JOptionPane.showMessageDialog(null, "Error no ha escogido a quien enviar", "ERROR", 1);
        }
    }//GEN-LAST:event_BT_EnviarActionPerformed

    private void RB_UsuarioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_RB_UsuarioActionPerformed
        if(RB_Usuario.isSelected()){
            RB_Lista.setSelected(false);
            CB_Envios.removeAllItems();
            var dataUser = Data.getData();
            var user = dataUser.getUser();
            var strError = "";
            var objManejoArchivo = new ManejoArchivo();
            File Archivo = new File("C:/MEIA/contactos.txt");
            var lista = objManejoArchivo.LecturaCompleta(Archivo, strError);
            for (int i = 0; i < lista.size(); i++) {
                var splitAux = lista.get(i).split(Pattern.quote("|"));
                if(splitAux[0].equals(user) && splitAux[4].equals("1")){
                   CB_Envios.addItem(splitAux[1]);
                }
            }

            Archivo = new File("C:/MEIA/bitacora_contactos.txt");
            lista = objManejoArchivo.LecturaCompleta(Archivo, strError);
            for (int i = 0; i < lista.size(); i++) {
                var splitAux = lista.get(i).split(Pattern.quote("|"));
                if(splitAux[0].equals(user) && splitAux[4].equals("1")){
                   CB_Envios.addItem(splitAux[1]);
                }
            }
        }
        else{
            CB_Envios.removeAllItems();
        }
    }//GEN-LAST:event_RB_UsuarioActionPerformed

