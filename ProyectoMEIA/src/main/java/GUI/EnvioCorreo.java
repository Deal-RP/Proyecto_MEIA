    private void TF_ArchivoKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_TF_ArchivoKeyPressed
        evt.consume();
        JFileChooser dialogo = new JFileChooser();
        File ficheroImagen;
        String rutaArchivo;
        int valor = dialogo.showOpenDialog(this);
        if (valor == JFileChooser.APPROVE_OPTION) 
        {
            ficheroImagen = dialogo.getSelectedFile();
            rutaArchivo = ficheroImagen.getPath();
            TF_Archivo.setText(rutaArchivo);
        }
    }//GEN-LAST:event_TF_ArchivoKeyPressed

